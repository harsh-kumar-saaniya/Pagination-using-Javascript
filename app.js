function pagination(querySet, page, rows) {
    var trimStart = (page - 1) * rows
    var trimEnd = trimStart + rows

    var trimmedData = querySet.slice(trimStart, trimEnd)
    var pages = Math.ceil(querySet.length / rows)

    console.log("here we go",pages);

    return {
        'querySet': trimmedData,
        'pages': pages
    }
    
}

function pageButtons(pages) {
    console.log();
    var wrapper = document.getElementById("pagination-wraper")
    wrapper.innerHTML = ''

    for (var page = 1; page <= pages; page++) {
        wrapper.innerHTML += `<button value=${page} class="btn">${page}</button>`
    }

    $('.page').on('click', function(){
        $('table').empty()
        state.page = $(this).val()
        buildTable()
    })
}


function buildTable() {

    var data = pagination(state.querySet, state.page, state.rows)
    console.log("data", data);

    mylist = data.querySet
    for (var i = 1 in mylist) {
        var temp = '';
        mylist.forEach((u) => {
            temp += "<tr>";
            temp += "<td>" + u.ID + "</td>";
            temp += "<td>" + u.Salesman + "</td>";
            temp += "<td>" + u.Product + "</td></tr>";
        });

        document.getElementById("table").innerHTML = temp;

    }
    pageButtons(data.pages)


}